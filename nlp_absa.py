ASPECTS = ['battery', 'screen', 'camera', 'performance', 'price', 'design']

ASPECT_KEYWORDS = {
    'battery': ['battery', 'charging', 'battery life'],
    'screen': ['screen', 'display'],
    'camera': ['camera', 'photo', 'picture'],
    'performance': ['performance', 'speed', 'lag'],
    'price': ['price', 'cost', 'expensive'],
    'design': ['design', 'build', 'look']
}

def extract_aspects(review):
    review_lower = review.lower()
    found = []

    for aspect, keywords in ASPECT_KEYWORDS.items():
        if any(word in review_lower for word in keywords):
            found.append(aspect)

    return found

product_df['aspects'] = product_df['review'].apply(extract_aspects)


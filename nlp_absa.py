def clean_text(text):
    text = str(text).lower()
    text = text.replace("\n", " ")
    return text


df['clean_sentence'] = df['sentence'].apply(clean_text)

# ==============================
# STEP 3: FILTER DOMAIN (Laptop/Restaurant)
# ==============================

# Assuming dataset has domain column (if not, manually split datasets)

if 'domain' in df.columns:
    df = df[df['domain'].isin(['laptop', 'restaurant'])]

# ==============================
# STEP 4: PREDEFINED ASPECTS
# ==============================

PREDEFINED_ASPECTS = [
    'battery', 'screen', 'price', 'performance', 'design',
    'food', 'service', 'ambience'
]

# Function to map aspect to predefined category

def map_aspect(aspect):
    for a in PREDEFINED_ASPECTS:
        if a in aspect.lower():
            return a
    return 'other'


df['mapped_aspect'] = df['aspect'].apply(map_aspect)

# ==============================
# STEP 5: SENTIMENT USING VADER
# ==============================

sia = SentimentIntensityAnalyzer()

def get_sentiment(text):
    score = sia.polarity_scores(text)['compound']
    if score >= 0.05:
        return 'positive'
    elif score <= -0.05:
        return 'negative'
    else:
        return 'neutral'


df['predicted_sentiment'] = df['clean_sentence'].apply(get_sentiment)

# ==============================
# STEP 6: AGGREGATION
# ==============================

result = {}

for aspect in df['mapped_aspect'].unique():
    subset = df[df['mapped_aspect'] == aspect]
    result[aspect] = {
        'positive': sum(subset['predicted_sentiment'] == 'positive'),
        'negative': sum(subset['predicted_sentiment'] == 'negative'),
        'neutral': sum(subset['predicted_sentiment'] == 'neutral')
    }

print("\nAggregated Results:")
print(result)

# Convert to DataFrame for plotting
plot_df = pd.DataFrame(result).T
plot_df = plot_df.fillna(0)

